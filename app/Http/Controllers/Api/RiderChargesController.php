     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $this->RiderChargeObj->deleteRiderCharge($id);
        return $this->sendResponse('Success', 'Rider charge deleted successfully');
    }
